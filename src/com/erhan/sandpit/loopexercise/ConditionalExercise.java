package com.erhan.sandpit.loopexercise;

public class ConditionalExercise {
    public static void main (String[] args) {
        int num = 12;

        if (num > 10) {
            System.out.print("Hello ");
        } else if (num > 20) {
            System.out.print("world.");
        } else {
            System.out.print("!");
        }
        System.out.println("world!");
    }
}
